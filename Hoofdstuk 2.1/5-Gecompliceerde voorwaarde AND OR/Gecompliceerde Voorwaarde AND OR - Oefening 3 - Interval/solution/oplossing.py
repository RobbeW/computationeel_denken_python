# Vraag de gebruiker om een decimaal getal
getal = float(input("Voer een decimaal getal in: "))

# Controleer of het getal tussen 8 (inbegrepen) en 14 (niet inbegrepen) ligt
if 8 <= getal < 14:
    print("Het getal ligt tussen 8 (inbegrepen) en 14 (niet inbegrepen).")
else:
    print("Het getal ligt niet tussen 8 (inbegrepen) en 14 (niet inbegrepen).")
