# Criar uma variável chamada mensagem e atribuir-lhe a cadeia de caracteres "Olá, o meu nome é "
mensagem = "Olá, o meu nome é "

# Pedir ao utilizador que introduza o seu nome
nome = input("Introduza o seu nome: ")

# Acrescentar o nome introduzido à variável message
mensagem = mensagem + nome

# Imprimir o conteúdo da variável mensagem no ecrã
print(mensagem)
