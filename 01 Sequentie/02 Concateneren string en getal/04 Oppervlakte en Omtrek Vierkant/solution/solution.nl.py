# Vraag de gebruiker naar de lengte van de zijde van het vierkant
zijde = float(input("Voer de lengte van de zijde in: "))

# Bereken de oppervlakte en omtrek van het vierkant
oppervlakte = zijde * zijde
omtrek = 4 * zijde

# Print de oppervlakte en omtrek van het vierkant
print("De omtrek van het vierkant is", omtrek, "centimeter.")
print("De oppervlakte van het vierkant is", oppervlakte, "vierkante centimeter.")